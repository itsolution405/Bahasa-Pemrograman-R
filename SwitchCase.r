# Bisa gunakan Numerik / Angka

# Integer (bilangan bulat): seperti 1, 2, 3, dll.
# Float (bilangan desimal): seperti 3.14, -0.5, dll.
# Double (bilangan desimal presisi ganda): seperti 3.14159, dll.

angka = 3

switch (angka,
{print("saya")},
{print("seorang")},
{print("mahasiswa")}
)

# Output adalah seorang

# Karena ketika kita menggunakan numerik atau bilangan bulat angka yang di tunjukan perintah ke 3 yaitu seorang



